    <h2>Baumstreit in Adorf: Stadt ordnet Fällung einer Eiche an</h2>
    <img src="images/eiche.JPG" alt="Eiche an der Grundstücksgrenze" class="article-image-small">
  </div>
  <div class="article-body">
    <p>
      In der kreisfreien Stadt Adorf (Bayern) ist ein Streit zwischen zwei Grundstücksnachbarn eskaliert. Herr Anton Müller, 
      Eigentümer eines Grundstücks mit der Flurnummer 111, steht im Zentrum eines Verwaltungsverfahrens. Auf seinem Grundstück befindet sich 
      eine Eiche, die unmittelbar an der Grenze zum Nachbargrundstück von Herrn Michael Schreiner steht.
    </p>
    <p>
      Herr Schreiner hatte sich bei der Stadtverwaltung darüber beschwert, dass regelmäßig Laub und Äste der Eiche auf sein Grundstück 
      gelangen. Trotz wiederholter Hinweise sei Herr Müller nicht aktiv geworden, um das Problem zu beheben.
    </p>
    <p>
      Nach Prüfung des Falls und unter Berücksichtigung der Beschwerde kam die Stadtverwaltung zu dem Schluss, dass das Anliegen von 
      Herrn Schreiner berechtigt ist. Infolgedessen leitete die Stadt ein Verwaltungsverfahren ein. 
    </p>
    <p>
      Das Ergebnis: Die Stadt Adorf hat per schriftlichem Bescheid angeordnet, dass Herr Müller die Eiche auf seinem Grundstück fällen 
      muss. Ob Herr Müller gegen den Bescheid Rechtsmittel einlegen wird, ist bislang nicht bekannt.
    </p>
  </div>
